/* Chapter 31 Pg 206-207 Equ 31.1 31.2 , 31.3 using VSOP 87
 * Calculate earth rectangular heliocentric ecliptical coordinates
 * for given julian day. Values are in UA.
 * params : Julian day, rect coords */
void get_earth_helio_coords(double JD, double * X, double * Y, double * Z)
{
	double t, t2, t3, t4, t5;
	double L0, L1, L2, L3, L4, L5;
	double B0, B1, B2, B3, B4, B5;
	double R0, R1, R2, R3, R4, R5;
	double L,B,R;

	/* get julian ephemeris day */
	t = (JD - 2451545.0) / 365250;
	t2 = t * t;
	t3 = t2 * t;
	t4 = t3 * t;
	t5 = t4 * t;

	/* calc L series */
	L0 = calc_series (earth_longitude_l0, LONG_L0, t);
	L1 = calc_series (earth_longitude_l1, LONG_L1, t);
	L2 = calc_series (earth_longitude_l2, LONG_L2, t);
	L3 = calc_series (earth_longitude_l3, LONG_L3, t);
	L4 = calc_series (earth_longitude_l4, LONG_L4, t);
	L5 = calc_series (earth_longitude_l5, LONG_L5, t);
	L = (L0 + L1 * t + L2 * t2 + L3 * t3 + L4 * t4 + L5 * t5);

	/* calc B series */
	B0 = calc_series (earth_latitude_b0, LAT_B0, t);
	B1 = calc_series (earth_latitude_b1, LAT_B1, t);
	B2 = calc_series (earth_latitude_b2, LAT_B2, t);
	B3 = calc_series (earth_latitude_b3, LAT_B3, t);
	B4 = calc_series (earth_latitude_b4, LAT_B4, t);
	B5 = calc_series (earth_latitude_b5, LAT_B5, t);
	B = (B0 + B1 * t + B2 * t2 + B3 * t3 + B4 * t4 + B5 * t5);

	/* calc R series */
	R0 = calc_series (earth_radius_r0, RADIUS_R0, t);
	R1 = calc_series (earth_radius_r1, RADIUS_R1, t);
	R2 = calc_series (earth_radius_r2, RADIUS_R2, t);
	R3 = calc_series (earth_radius_r3, RADIUS_R3, t);
	R4 = calc_series (earth_radius_r4, RADIUS_R4, t);
	R5 = calc_series (earth_radius_r5, RADIUS_R5, t);
	R = (R0 + R1 * t + R2 * t2 + R3 * t3 + R4 * t4 + R5 * t5);

	/* change to fk5 reference frame */
	vsop87_to_fk5(&L, &B, JD);

	/* convert to rectangular coord */
	sphe_to_rect(L, B, R, X, Y, Z);
}

